              <div class="card-body">
<p class="card-text">렘넌트서밋위원회(이하 ‘회사’라 함)는 회원의 개인정보 보호를 매우 중요시하며, 개인정보 보호 관련 주요법률인 “개인정보보호법” 등 관련 법령을 준수하고 있습니다.</p>
<p class="card-text">회사는 개인정보처리방침을 수립하여 회원의 개인정보를 보호하고 있으며, 이를 홈페이지에 명시하여 회원이 온라인상에서 회사에 제공한 개인정보가 어떠한 용도와 방식으로 이용되고 있으며 개인정보보호를 위해 어떠한 조치를 취하는지 알려드립니다.</p>
<p class="card-text">회사의 개인정보처리방침은 다음과 같은 내용을 담고 있습니다.</p>
<p></p>
<p class="card-text">1. 수집하는 개인정보 항목 및 수집방법</p>
‘개인정보’란 살아 있는 개인에 관한 정보로서 성명, 생년월일 및 사진 등을 통하여 당해 개인을 식별할 수 있는 정보(당해 정보만으로는 특정 개인을 알아볼 수 없는 경우에도 다른 정보와 쉽게 결합하여 알아볼 수 있는 것을 포함)를 말합니다.</p>
<p></p>
<p class="card-text">(1)수집하는 개인정보 항목</p>
<p class="card-text">회사는 렘넌트 관리를 위해 아래와 같은 개인정보를 수집하고 있습니다.</p>
<p class="card-text">성명, 생년월일, 성별, 이메일, 휴대폰번호, 교구정보, 학교정보, 직장정보, 가족사항, 영적정보 등</p>
<p class="card-text">(2) 서비스 이용과정이나 사업 처리과정에서 아래와 같은 정보들이 생성되어 수집될 수 있습니다.</p>
<p class="card-text">서비스 이용기록, 접속로그, 엑셀파일다운로드기록</p>
<p class="card-text">(3) 개인정보 수집방법</p>
<p class="card-text">회사는 다음과 같은 방법으로 개인정보를 수집합니다.</p>
<p class="card-text">교회에서 제공하는 엑셀 파일</p>
<p class="card-text">렘넌트서밋위원회 시스템을 통해 사용자가 입력하는 정보</p>
<p></p>
<p class="card-text">2. 개인정보의 수집 및 이용목적</p>
<p class="card-text">회사는 수집한 개인정보를 다음의 목적을 위해 활용합니다.</p>
<p class="card-text">렘넌트들의 개인정보관리, 영적상태 관리, 공지사항 전달, 출석체크, 예배신청 등</p>
<p></p>
<p class="card-text">3. 개인정보의 보유 및 이용기간</p>
<p class="card-text">원칙적으로 개인정보 수집 및 이용목적이 달성된 후에는 해당 정보를 지체 없이 파기합니다. 단, 다음의 정보에 대해서는 아래의 이유로 명시한 기간 동안 보존합니다.</p>
<p></p>
<p class="card-text">회사 내부방침에 의한 정보보유 사유</p>
<p class="card-text">회원이 교회에 출석하는 경우 회사는 원활한 서비스의 제공을 위해 회원정보를 교회를 이명하기 전까지 보관합니다.</p>
<p></p>
<p class="card-text">4. 개인정보의 공유, 제공 및 관리</p>
<p class="card-text">회사는 회원의 동의가 있거나 관련법령의 규정에 의한 경우를 제외하고는 개인정보를 수집 및 이용목적으로 정한 범위 내에서 사용하며, 동 범위를 초과하여 이용하거나 외부에 공개/제공하지 않습니다.</p>
<p class="card-text">렘넌트서밋위원회 홈페이지는 지정된 사용자만 이용이 가능하며 사용자별로 해당 정보에 대한 이용권한을 제한하며 최초 로그인시 개인정보보호서약 화면을 통해 서약을 받으며 서약 동의 날짜를 저장하며 사용자가 정기적으로 비밀번호를 바꿀 수 있도록 합니다.</p>
<p></p>
<p class="card-text">5. 개인정보의 처리위탁</p>
<p class="card-text">회사는 회원 관리 및 민원사항에 대한 처리 등 원활한 업무 수행을 위하여 아래와 같이 개인정보 처리 업무를 위탁하여 운영하고 있습니다. 또한 위탁계약 시 개인정보보호의 안전을 기하기 위하여 개인정보보호 관련 법규의 준수, 개인정보에 관한 제3자 공급 금지 및 사고시의 책임부담 등을 명확히 규정하고 있습니다. 동 업체가 변경될 경우, 변경된 업체명을 공지사항 내지 개인정보처리방침 화면을 통해 고지하겠습니다.</p>
<p></p>
<p class="card-text">● 수탁업체 : 네오프레임미디어(주)</p>
<p class="card-text">● 위탁업무 : 시스템 관리 및 유지보수</p>
<p></p>
<p class="card-text">6. 개인정보의 파기 절차 및 방법</p>
<p class="card-text">회사는 원칙적으로 개인정보 수집 및 이용목적이 달성된 후에는 해당 정보를 지체 없이 파기합니다.</p>
<p></p>
<p class="card-text">7. 개인정보 보호책임자 및 담당 부서</p>
<p class="card-text">회사는 회원의 개인정보를 보호하고 개인정보와 관련한 불만을 처리하기 위하여 개인정보 보호책 임자를 지정하고 있습니다. 회사의 서비스를 이용하시며 발생하는 모든 개인정보보호 관련 민원은 아래 개인정보 보호책임자 및 담당 부서로 연락해 주시기 바랍니다. 회사는 회원의 문의사항에 대해 신속하게 충분한 답변을 드릴 것입니다.</p>
